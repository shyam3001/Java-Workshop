    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        String val = jList1.getSelectedValue();
        if (val == null) return;
        String name = val.split("\\(")[0];
        try {
            Statement stmt = conn.createStatement();
            ResultSet set = stmt.executeQuery("select * from student where name='" + name + "'");
            set.next();
            jTextField1.setText(set.getString("studentid"));
            jTextField2.setText(set.getString("name"));
            jSpinner1.setValue(set.getInt("age"));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jList1ValueChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int studentid = Integer.parseInt(jTextField1.getText());
        String name = jTextField2.getText();
        int age = (Integer) jSpinner1.getValue(); // casting

        try {
            Statement stmt = conn.createStatement();
            stmt.executeUpdate("INSERT INTO student VALUES(" + studentid + ",'" + name + "'," + age + ") "
                    + "ON DUPLICATE KEY UPDATE name='" + name + "', age=" + age);

            JOptionPane.showMessageDialog(null, "Successfully Updated!");
            loadData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int res = JOptionPane.showConfirmDialog(null, "Are you Sure?");
        if (res == JOptionPane.YES_OPTION) {
            try {
                int studentid = Integer.parseInt(jTextField1.getText());
                Statement stmt = conn.createStatement();
                stmt.executeUpdate("DELETE from student where studentid="+studentid);
                loadData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

